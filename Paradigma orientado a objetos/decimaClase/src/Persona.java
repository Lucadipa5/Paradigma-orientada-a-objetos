public class Persona {
private int cantDinero;
private int Edad;

    public Persona(int cantDinero, int edad) {
        this.cantDinero = cantDinero;
        this.Edad = edad;
    }

    public int getCantDinero() {
        return cantDinero;
    }

    public int getEdad() {
        return Edad;
    }
}
